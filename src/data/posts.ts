export function getPosts(): Post[] {
  return [...posts].sort((a, b) => b.publishedAt.localeCompare(a.publishedAt));
}

export function getPost(slug: string): Post | undefined {
  return posts.find((p) => p.slug === slug);
}

/**
 * 関連記事（内部リンク強化）。
 * 共有キーワード数 → 共有プラン数 の順でスコアリングし、上位を返す。
 */
export function getRelatedPosts(slug: string, limit = 3): Post[] {
  const base = getPost(slug);
  if (!base) return [];
  return posts
    .filter((p) => p.slug !== slug)
    .map((p) => {
      const sharedKeywords = p.keywords.filter((k) =>
        base.keywords.includes(k),
      ).length;
      const sharedPlans = p.relatedPlans.filter((pl) =>
        base.relatedPlans.includes(pl),
      ).length;
      return { post: p, score: sharedKeywords * 2 + sharedPlans };
    })
    .filter((x) => x.score > 0)
    .sort((a, b) => b.score - a.score || b.post.publishedAt.localeCompare(a.post.publishedAt))
    .slice(0, limit)
    .map((x) => x.post);
}
